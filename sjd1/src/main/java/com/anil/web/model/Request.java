package com.anil.web.model;

public class Request {
	private int id;
	private int rn;
	private String name;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getRn() {
		return rn;
	}
	public void setRn(int rn) {
		this.rn = rn;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	@Override
	public String toString() {
		return "Request [id=" + id + ", rn=" + rn + ", name=" + name + "]";
	}
	

}
